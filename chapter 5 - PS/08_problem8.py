# Can you change the values inside a list which is contained in set S? s = {8, 7, 12, "Harry", [1,2]} 
s = {8, 7, 12, "Harry", [1,2]}

# no, you cannot change the values inside a list that is inside a set, because  lists are mutable (i.e. they can be changed), and sets only allow immutable (unchangeable) items.